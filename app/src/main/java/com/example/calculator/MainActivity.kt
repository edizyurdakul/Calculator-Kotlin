class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            CalculatorTheme {
                val calculatorViewModel = viewModel<CalculatorViewModel>()
                val state = calculatorViewModel.state
                val buttonSpacing = 8.dp
                Calculator(state = state, buttonSpacing = buttonSpacing, modifier = Modifier.fillMaxSize().padding(16.dp), onAction = calculatorViewModel::onAction)

            }
        }
    }
}

